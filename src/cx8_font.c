/* ─── Public API ───────────────────────────────────────────── */

const uint8_t *cx8_font_glyph(char ch)
{
    int idx = (unsigned char)ch - 32;
    if (idx < 0 || idx >= 95)
        idx = 0; /* fallback to space */
    return s_font[idx];
}

void cx8_font_draw_char(char ch, int x, int y, uint8_t color, cx8_plot_fn plot)
{
    const uint8_t *glyph = cx8_font_glyph(ch);
    for (int row = 0; row < CX8_GLYPH_H; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < CX8_GLYPH_W; col++) {
            if (bits & (0x8 >> col))
                plot(x + col, y + row, color);
        }
    }
}
